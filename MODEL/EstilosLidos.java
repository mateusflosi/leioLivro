package Model;

public class EstilosLidos {
    private String estilo;
    private int qtd;
    
    public void setEstilo(String estilo){
        this.estilo = estilo;
    }
    
    public void setQtd(int qtd){
        this.qtd = qtd;
    }
    
    public String getEstilo(){
        return estilo;
    }
    
    public int getQtd(){
        return qtd;
    }
}
