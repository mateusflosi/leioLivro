package Model;

public class LivroLista {
    private String titulo;
    private String img;
    
    public void setTitulo(String titulo){
        this.titulo = titulo;
    }
    
    public void setImg(String img){
        this.img = img;
    }
    
    public String getTitulo(){
        return titulo;
    }
    
    public String getImg(){
        return img;
    }
}